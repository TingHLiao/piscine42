void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	char *s;

	s = argv[0];
	if (argc == 1)
	{
		while (*s != '\0')
			ft_putchar(*(s++));
		ft_putchar('\n');
	}
	return (0);
}
