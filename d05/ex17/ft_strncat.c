char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;

	i = 0;
	while (dest[i] != '\0')
		i++;
	while (*src != '\0' && nb--)
	{
		dest[i++] = *(src++);
	}
	dest[i] = '\0';
	return (dest);
}
