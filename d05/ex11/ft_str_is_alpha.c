int		ft_str_is_alpha(char *str)
{
	int		flag;
	char	c;

	flag = 1;
	while (*str != '\0')
	{
		c = *(str++);
		if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
			flag = 1;
		else
			return (0);
	}
	return (flag);
}
