int		ft_str_is_lowercase(char *str)
{
	int		flag;
	char	c;

	flag = 1;
	while (*str != '\0')
	{
		c = *(str++);
		if (c >= 'a' && c <= 'z')
			flag = 1;
		else
			return (0);
	}
	return (flag);
}
