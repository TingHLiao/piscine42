int		ft_str_is_printable(char *str)
{
	int		flag;
	char	c;

	flag = 1;
	while (*str != '\0')
	{
		c = *(str++);
		if (c >= 32 && c <= 126)
			flag = 1;
		else
			return (0);
	}
	return (flag);
}
