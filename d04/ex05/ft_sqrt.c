int		ft_sqrt(int nb)
{
	unsigned int i;

	if (nb <= 0)
	{
		return (0);
	}
	i = 1;
	while (i * i <= (unsigned int)nb)
	{
		if (i * i == (unsigned int)nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
