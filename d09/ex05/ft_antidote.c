int		ft_antidote(int i, int j, int k)
{
	if (i > j)
	{
		i = i + j;
		j = i - j;
		i = i - j;
	}
	if (i > k)
	{
		i = i + k;
		k = i - k;
		i = i - k;
	}
	if (j > k)
	{
		j = j + k;
		k = j - k;
		j = j - k;
	}
	return (j);
}
