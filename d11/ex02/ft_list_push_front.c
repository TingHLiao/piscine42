#include <stdlib.h>
#include "ft_list.h"

t_list	*ft_create_elem(void *data);

void	ft_list_push_front(t_list **begin_list, void *data)
{
	t_list *new_begin;

	new_begin = ft_create_elem(data);
	new_begin->next = *begin_list;
	*begin_list = new_begin;
}
